async def generate_voiceover_edge(text: str, output_path: Path, voice: str = "en-US-ChristopherNeural") -> Path:
    """Generate voiceover using Edge-TTS (free, no API key needed)."""
    import edge_tts

    for attempt in range(3):
        try:
            communicate = edge_tts.Communicate(text, voice, rate="+10%")
            await communicate.save(str(output_path))
            # Validate output
            if output_path.exists() and output_path.stat().st_size > 100:
                return output_path
            raise RuntimeError(f"Edge-TTS produced empty file ({output_path.stat().st_size if output_path.exists() else 'missing'} bytes)")
        except Exception as e:
            if attempt < 2:
                wait = 3 * (attempt + 1)
                print(f"        Edge-TTS attempt {attempt+1} failed: {e}, retrying in {wait}s...")
                await asyncio.sleep(wait)
            else:
                raise


async def generate_all_voiceovers_edge(
    texts: list[str], output_paths: list[Path], voice: str = "en-US-ChristopherNeural"
) -> list[Path]:
    """Generate all voiceovers sequentially in a single async session."""
    results = []
    for i, (text, path) in enumerate(zip(texts, output_paths)):
        result = await generate_voiceover_edge(text, path, voice)
        results.append(result)
        # Small delay between requests to avoid rate limiting
        await asyncio.sleep(1.5)
    return results


def generate_voiceover_elevenlabs(text: str, output_path: Path, voice_id: str = "pNInz6obpgDQGcFmaJgB") -> Path:
    """Generate voiceover using ElevenLabs API (premium quality)."""
    from elevenlabs import ElevenLabs

    api_key = os.environ.get("ELEVENLABS_API_KEY")
    if not api_key:
        raise ValueError("ELEVENLABS_API_KEY not set")

    client = ElevenLabs(api_key=api_key)
    audio_gen = client.text_to_speech.convert(
        voice_id=voice_id,
        text=text,
        model_id="eleven_multilingual_v2",
    )

    with open(output_path, "wb") as f:
        for chunk in audio_gen:
            f.write(chunk)

    return output_path


