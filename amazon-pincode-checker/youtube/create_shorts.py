def get_audio_duration(audio_path: Path) -> float:
    """Get duration of audio file in seconds."""
    import moviepy
    clip = moviepy.AudioFileClip(str(audio_path))
    duration = clip.duration
    clip.close()
    return duration


def assemble_video(
    scenes: list[dict],
    scene_images: list[Path],
    scene_audios: list[Path],
    output_path: Path,
    title: str = "",
) -> Path:
    """Assemble scene images and audio into a vertical video."""
    import moviepy

    clips = []

    for i, (scene, img_path, audio_path) in enumerate(zip(scenes, scene_images, scene_audios)):
        audio = moviepy.AudioFileClip(str(audio_path))
        duration = audio.duration

        # Static image clip with audio — fast and reliable
        img_clip = (
            moviepy.ImageClip(str(img_path))
            .with_duration(duration)
            .with_audio(audio)
        )
        clips.append(img_clip)

    # Concatenate all scene clips
    final = moviepy.concatenate_videoclips(clips, method="compose")

    # Ensure it's under 60 seconds for Shorts
    if final.duration > 60:
        final = final.subclipped(0, 60)

    final.write_videofile(
        str(output_path),
        fps=24,
        codec="libx264",
        audio_codec="aac",
        preset="medium",
        threads=4,
        logger=None,
    )

    # Clean up
    final.close()
    for clip in clips:
        clip.close()

    return output_path


