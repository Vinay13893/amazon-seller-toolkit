# ---------------------------------------------------------------------------
# Script parser — extract structured scenes from markdown scripts
# ---------------------------------------------------------------------------

def parse_scripts(scripts_text: str) -> list[dict]:
    """
    Parse the markdown scripts file into structured video data.

    Expected format per script block (separated by ---):
        ## N. Title
        **Hook (0-3 sec):** ...
        **Visual:** ...
        **Body (3-45 sec):** ...
        **Visual Sequence:** ...
        **Climax/Reveal (45-55 sec):** ...
        **Visual:** ...
        **CTA (55-60 sec):** ...
    """
    videos = []

    # Split by --- separators
    script_blocks = re.split(r"\n---\n", scripts_text)

    for block in script_blocks:
        block = block.strip()
        if not block:
            continue

        # Extract title
        title_match = re.search(r"##\s*\d*\.?\s*(.+)", block)
        if not title_match:
            continue
        title = title_match.group(1).strip()

        # Split block into labelled sections by **Label:** pattern
        # Each section: (label, content)
        sections: list[tuple[str, str]] = []
        parts = re.split(r"\*\*(.+?)\*\*:?\s*", block)
        # parts = [preamble, label1, content1, label2, content2, ...]
        for i in range(1, len(parts) - 1, 2):
            label = parts[i].strip().lower()
            content = parts[i + 1].strip()
            sections.append((label, content))

        # Helper to find section by keyword
        def _find(keyword: str) -> str:
            for lbl, txt in sections:
                if keyword in lbl:
                    return txt
            return ""

        hook_vo = _find("hook")
        hook_visual = _find("visual")  # first "visual" after hook
        body_vo = _find("body")
        climax_vo = _find("climax")
        cta_vo = _find("cta")

        # Find visual sequence specifically
        body_visual = ""
        climax_visual = ""
        for lbl, txt in sections:
            if "visual sequence" in lbl:
                body_visual = txt
            elif "visual" in lbl and climax_vo and sections.index((lbl, txt)) > next(
                (i for i, (l, _) in enumerate(sections) if "climax" in l), 999
            ):
                climax_visual = txt

        # Build scene list
        scenes = []

        # Scene 1: Hook
        if hook_vo:
            scenes.append({
                "type": "hook",
                "voiceover": _clean_vo(hook_vo),
                "visual_desc": hook_visual or "Dramatic opening shot",
                "duration_hint": 3,
            })

        # Scene 2-4: Body (split into sub-scenes)
        if body_vo:
            body_sentences = _split_into_sentences(body_vo)
            body_visuals = (body_visual or "").split("→")
            for i, sentence in enumerate(body_sentences):
                sentence = _clean_vo(sentence)
                if not sentence or len(sentence) < 10:
                    continue
                visual = body_visuals[i].strip() if i < len(body_visuals) else "Build montage"
                scenes.append({
                    "type": "body",
                    "voiceover": sentence,
                    "visual_desc": visual,
                    "duration_hint": 8,
                })

        # Scene 5: Climax
        if climax_vo:
            scenes.append({
                "type": "climax",
                "voiceover": _clean_vo(climax_vo),
                "visual_desc": climax_visual or "Big reveal shot",
                "duration_hint": 10,
            })

        # Scene 6: CTA
        if cta_vo:
            scenes.append({
                "type": "cta",
                "voiceover": _clean_vo(cta_vo),
                "visual_desc": "Text overlay with question",
                "duration_hint": 5,
            })

        if scenes:
            videos.append({
                "title": title,
                "scenes": scenes,
                "full_voiceover": " ".join(s["voiceover"] for s in scenes),
            })

    return videos


def _clean_vo(text: str) -> str:
    """Clean voiceover text — remove markdown, SFX tags for TTS."""
    text = re.sub(r"\[SFX:.*?\]", "", text)
    text = re.sub(r"\*\*.*?\*\*:?\s*", "", text)
    text = re.sub(r"\*", "", text)
    text = re.sub(r"\s+", " ", text).strip()
    return text


def _split_into_sentences(text: str) -> list[str]:
    """Split body text into logical sentences/chunks."""
    text = _clean_vo(text)
    # Split on periods, but keep the sentence together if short
    raw = re.split(r"(?<=[.!?])\s+", text)
    # Merge very short sentences
    merged = []
    buffer = ""
    for s in raw:
        buffer = (buffer + " " + s).strip() if buffer else s
        if len(buffer) > 40:
            merged.append(buffer)
            buffer = ""
    if buffer:
        merged.append(buffer)
    return merged if merged else [text]


