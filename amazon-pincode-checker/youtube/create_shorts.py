#!/usr/bin/env python3
"""
YouTube Shorts Video Creator — AI-powered video production from scripts.

Takes the scripts generated by youtube_viral_titles.py (Step 6) and turns
each one into a ready-to-upload vertical video (1080×1920) with:
  - AI voiceover (ElevenLabs or free Edge-TTS)
  - AI-generated scene images (Gemini) or styled text slides
  - Automatic assembly with transitions and subtitles

Requirements:
  pip install edge-tts Pillow moviepy
  # Optional for better quality:
  pip install elevenlabs google-genai

  For AI image generation (optional):
    GEMINI_API_KEY env var

  For premium voiceover (optional):
    ELEVENLABS_API_KEY env var

Usage:
  # Create videos from scripts file
  python create_shorts.py --scripts calmcreations15_results/scripts.md

  # Use ElevenLabs voice (premium quality)
  python create_shorts.py --scripts scripts.md --voice elevenlabs

  # Generate AI images for scenes
  python create_shorts.py --scripts scripts.md --ai-images

  # Create only the first N videos
  python create_shorts.py --scripts scripts.md --limit 3

  # Custom output directory
  python create_shorts.py --scripts scripts.md --output-dir ./my_videos
"""

