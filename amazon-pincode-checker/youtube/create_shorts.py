def _load_font(name: str, size: int):
    """Try to load a TrueType font, fall back to default."""
    from PIL import ImageFont
    for path in [name, f"C:/Windows/Fonts/{name}"]:
        try:
            return ImageFont.truetype(path, size)
        except (OSError, IOError):
            continue
    return ImageFont.load_default()


def _draw_gradient(draw, width: int, height: int, color_top: tuple, color_bot: tuple):
    """Draw a vertical gradient background."""
    for y in range(height):
        ratio = y / height
        r = int(color_top[0] + (color_bot[0] - color_top[0]) * ratio)
        g = int(color_top[1] + (color_bot[1] - color_top[1]) * ratio)
        b = int(color_top[2] + (color_bot[2] - color_top[2]) * ratio)
        draw.line([(0, y), (width, y)], fill=(r, g, b))


def _draw_text_with_shadow(draw, pos, text: str, font, fill, shadow_color=(0, 0, 0), offset: int = 3, **kwargs):
    """Draw text with a drop shadow for readability."""
    x, y = pos
    # Shadow
    draw.text((x + offset, y + offset), text, fill=shadow_color, font=font, **kwargs)
    # Main text
    draw.text((x, y), text, fill=fill, font=font, **kwargs)


def _draw_glow_circle(img, cx: int, cy: int, radius: int, color: tuple, alpha: int = 30):
    """Draw a soft glowing circle for visual interest."""
    from PIL import Image, ImageDraw
    overlay = Image.new("RGBA", img.size, (0, 0, 0, 0))
    od = ImageDraw.Draw(overlay)
    for r in range(radius, 0, -4):
        a = int(alpha * (r / radius))
        od.ellipse(
            [cx - r, cy - r, cx + r, cy + r],
            fill=(*color, a),
        )
    # Composite onto RGB image
    rgba = img.convert("RGBA")
    rgba = Image.alpha_composite(rgba, overlay)
    return rgba.convert("RGB")


def create_text_slide(
    text: str,
    output_path: Path,
    scene_type: str = "body",
    title: str = "",
    subtitle: str = "",
    scene_index: int = 0,
    total_scenes: int = 1,
    width: int = 1080,
    height: int = 1920,
) -> Path:
    """Create a professional styled text slide image for a scene."""
    from PIL import Image, ImageDraw
    import random

    # Visual themes per scene type: gradient colors + accent + glow
    themes = {
        "hook": {
            "grad_top": (10, 5, 30),
            "grad_bot": (40, 15, 5),
            "accent": (255, 165, 0),
            "text": (255, 255, 255),
            "glow": (255, 120, 0),
        },
        "body": {
            "grad_top": (5, 10, 35),
            "grad_bot": (10, 25, 50),
            "accent": (80, 180, 255),
            "text": (240, 240, 240),
            "glow": (50, 130, 255),
        },
        "climax": {
            "grad_top": (35, 5, 5),
            "grad_bot": (50, 10, 25),
            "accent": (255, 60, 60),
            "text": (255, 255, 255),
            "glow": (255, 40, 40),
        },
        "cta": {
            "grad_top": (5, 30, 15),
            "grad_bot": (10, 45, 30),
            "accent": (80, 255, 120),
            "text": (255, 255, 255),
            "glow": (50, 200, 80),
        },
    }
    theme = themes.get(scene_type, themes["body"])

    img = Image.new("RGB", (width, height), (0, 0, 0))
    draw = ImageDraw.Draw(img)

    # 1. Gradient background
    _draw_gradient(draw, width, height, theme["grad_top"], theme["grad_bot"])

    # 2. Glow effects for visual depth
    random.seed(hash(text) % 10000)  # Deterministic per scene
    for _ in range(3):
        gx = random.randint(100, width - 100)
        gy = random.randint(200, height - 200)
        gr = random.randint(200, 500)
        img = _draw_glow_circle(img, gx, gy, gr, theme["glow"], alpha=20)
    draw = ImageDraw.Draw(img)

    # 3. Accent bar at top
    draw.rectangle([(0, 0), (width, 6)], fill=theme["accent"])

    # Load fonts
    font_main = _load_font("arialbd.ttf", 56)     # Bold for main text
    font_title = _load_font("arial.ttf", 34)
    font_small = _load_font("arial.ttf", 28)
    font_sub = _load_font("arial.ttf", 32)
    font_label = _load_font("arialbd.ttf", 44)

    # 4. Scene counter top-right
    counter = f"{scene_index + 1}/{total_scenes}"
    draw.text((width - 60, 40), counter, fill=theme["accent"], font=font_small, anchor="rm")

    # 5. Title at top
    if title:
        title_wrapped = textwrap.fill(title, width=32)
        _draw_text_with_shadow(
            draw, (width // 2, 100), title_wrapped,
            fill=theme["accent"], font=font_title,
            anchor="mm", align="center", offset=2,
        )

    # 6. Scene type label (large, above center text)
    label = {
        "hook": "[ HOOK ]",
        "body": "[ BUILD ]",
        "climax": "[ REVEAL ]",
        "cta": "[ YOUR TURN ]",
    }.get(scene_type, "")
    if label:
        y_label = height // 2 - 280
        _draw_text_with_shadow(
            draw, (width // 2, y_label), label,
            fill=theme["accent"], font=font_label,
            anchor="mm", offset=2,
        )

    # 7. Decorative line under label
    line_y = height // 2 - 230
    line_w = 300
    draw.line(
        [(width // 2 - line_w // 2, line_y), (width // 2 + line_w // 2, line_y)],
        fill=theme["accent"], width=2,
    )

    # 8. Main text centered — large and bold
    wrapped = textwrap.fill(text, width=22)
    _draw_text_with_shadow(
        draw, (width // 2, height // 2 + 20), wrapped,
        fill=theme["text"], font=font_main,
        anchor="mm", align="center", offset=3,
    )

    # 9. Subtitle bar at bottom
    sub_text = subtitle or text
    sub_wrapped = textwrap.fill(sub_text, width=36)
    # Semi-transparent black bar
    bar_top = height - 180
    for y in range(bar_top, height):
        opacity = min(220, int(220 * (y - bar_top) / (height - bar_top)))
        draw.line([(0, y), (width, y)], fill=(0, 0, 0))
    # Subtitle text
    _draw_text_with_shadow(
        draw, (width // 2, height - 90), sub_wrapped,
        fill=(255, 255, 255), font=font_sub,
        anchor="mm", align="center", offset=2,
    )

    img.save(output_path, quality=95)
    return output_path


def generate_ai_image(prompt: str, output_path: Path) -> Path | None:
    """Generate an image using Gemini's image generation."""
    api_key = os.environ.get("GEMINI_API_KEY")
    if not api_key:
        return None

    try:
        from google import genai
        from google.genai import types

        client = genai.Client(api_key=api_key)
        response = client.models.generate_content(
            model="gemini-2.0-flash-preview-image-generation",
            contents=f"Generate a photorealistic vertical image (9:16 aspect ratio) for a YouTube Short. Scene: {prompt}. Style: cinematic, dramatic lighting, construction/DIY aesthetic. No text in image.",
            config=types.GenerateContentConfig(
                response_modalities=["IMAGE", "TEXT"],
            ),
        )

        # Extract image from response
        for part in response.candidates[0].content.parts:
            if part.inline_data and part.inline_data.mime_type.startswith("image/"):
                with open(output_path, "wb") as f:
                    f.write(part.inline_data.data)
                return output_path

    except Exception as e:
        print(f"    AI image generation failed ({e.__class__.__name__}), using text slide fallback")

    return None


def generate_scene_image(
    scene: dict,
    output_path: Path,
    title: str = "",
    use_ai_images: bool = False,
    scene_index: int = 0,
    total_scenes: int = 1,
) -> Path:
    """Generate a visual for a scene — AI image if enabled, otherwise styled text slide."""
    if use_ai_images:
        result = generate_ai_image(scene["visual_desc"], output_path)
        if result:
            return result

    # Fallback: styled text slide
    return create_text_slide(
        scene["voiceover"],
        output_path,
        scene_type=scene["type"],
        title=title,
        scene_index=scene_index,
        total_scenes=total_scenes,
    )


