# Main video creation pipeline
# ---------------------------------------------------------------------------

def create_video(
    video_data: dict,
    output_dir: Path,
    voice_engine: str = "edge",
    use_ai_images: bool = False,
    video_index: int = 1,
) -> Path | None:
    """Create a single YouTube Short video from parsed script data."""

    title = video_data["title"]
    scenes = video_data["scenes"]
    safe_title = re.sub(r'[<>:"/\\|?*]', '', title)[:60]

    print(f"\n  [{video_index}] Creating: {title}")

    # Create temp directory for assets
    work_dir = output_dir / f"temp_{video_index:02d}"
    work_dir.mkdir(parents=True, exist_ok=True)

    scene_images: list[Path] = []
    scene_audios: list[Path] = []

    try:
        # Generate all voiceovers
        print(f"      Generating voiceovers ({len(scenes)} scenes)...")
        audio_paths = []
        for i in range(len(scenes)):
            audio_paths.append(work_dir / f"scene_{i:02d}.mp3")

        if voice_engine == "edge":
            texts = [s["voiceover"] for s in scenes]
            asyncio.run(generate_all_voiceovers_edge(texts, audio_paths))
        else:
            for i, scene in enumerate(scenes):
                generate_voiceover_elevenlabs(scene["voiceover"], audio_paths[i])
        scene_audios = audio_paths

        # Generate all visuals
        print(f"      Generating visuals ({len(scenes)} scenes)...")
        for i, scene in enumerate(scenes):
            img_path = work_dir / f"scene_{i:02d}.png"
            generate_scene_image(
                scene, img_path, title=title, use_ai_images=use_ai_images,
                scene_index=i, total_scenes=len(scenes),
            )
            scene_images.append(img_path)

        # Assemble final video
        print(f"      Assembling video...")
        video_path = output_dir / f"{video_index:02d}_{safe_title}.mp4"
        assemble_video(scenes, scene_images, scene_audios, video_path, title=title)

        print(f"      [OK] Video saved: {video_path.name}")
        return video_path

    except Exception as e:
        print(f"      [FAIL] Failed: {e}")
        import traceback
        traceback.print_exc()
        return None

    finally:
        # Clean up temp files
        import shutil
        try:
            shutil.rmtree(work_dir)
        except Exception:
            pass


def run_video_creation(
    scripts_path: str,
    output_dir: str = "./shorts_videos",
    voice_engine: str = "edge",
    use_ai_images: bool = False,
    limit: int | None = None,
) -> None:
    """Main entry point — parse scripts, create videos."""

    scripts_file = Path(scripts_path)
    if not scripts_file.exists():
        print(f"ERROR: Scripts file not found: {scripts_path}")
        sys.exit(1)

    out = Path(output_dir)
    out.mkdir(parents=True, exist_ok=True)

    print("=" * 60)
    print("  YOUTUBE SHORTS VIDEO CREATOR")
    print("=" * 60)
    print(f"  Scripts:    {scripts_file}")
    print(f"  Output:     {out.resolve()}")
    print(f"  Voice:      {voice_engine}")
    print(f"  AI Images:  {'Yes' if use_ai_images else 'No (text slides)'}")
    if limit:
        print(f"  Limit:      {limit} videos")
    print("=" * 60)

    # Parse scripts
    scripts_text = scripts_file.read_text(encoding="utf-8")
    videos = parse_scripts(scripts_text)
    print(f"\n  Parsed {len(videos)} video scripts.")

    if not videos:
        print("  ERROR: No scripts found. Check the format of your scripts file.")
        sys.exit(1)

    if limit:
        videos = videos[:limit]
        print(f"  Limited to first {limit} videos.")

    # Create each video
    created = []
    failed = []

    for i, video_data in enumerate(videos, 1):
        result = create_video(
            video_data,
            output_dir=out,
            voice_engine=voice_engine,
            use_ai_images=use_ai_images,
            video_index=i,
        )
        if result:
            created.append(result)
        else:
            failed.append(video_data["title"])

    # Summary
    print("\n" + "=" * 60)
    print("  VIDEO CREATION COMPLETE")
    print("=" * 60)
    print(f"  Created:  {len(created)} videos")
    if failed:
        print(f"  Failed:   {len(failed)} videos")
    print(f"  Output:   {out.resolve()}")
    print(f"\n  Videos ready to upload:")
    for p in created:
        print(f"    > {p.name}")
    if failed:
        print(f"\n  Failed titles:")
        for t in failed:
            print(f"    X {t}")
    print("=" * 60)


