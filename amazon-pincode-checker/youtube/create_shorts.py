def main():
    parser = argparse.ArgumentParser(
        description="YouTube Shorts Video Creator — AI-powered video production",
        formatter_class=argparse.RawDescriptionHelpFormatter,
        epilog=(
            "Examples:\n"
            '  python create_shorts.py --scripts results/scripts.md\n'
            '  python create_shorts.py --scripts results/scripts.md --voice elevenlabs\n'
            '  python create_shorts.py --scripts results/scripts.md --ai-images --limit 3\n'
        ),
    )
    parser.add_argument(
        "--scripts", "-s", required=True,
        help="Path to the scripts markdown file from youtube_viral_titles.py",
    )
    parser.add_argument(
        "--output-dir", "-o", default="./shorts_videos",
        help="Directory for output video files (default: ./shorts_videos)",
    )
    parser.add_argument(
        "--voice", "-v", choices=["edge", "elevenlabs"], default="edge",
        help="Voice engine: 'edge' (free) or 'elevenlabs' (premium, needs API key)",
    )
    parser.add_argument(
        "--ai-images", action="store_true",
        help="Generate AI images for scenes using Gemini (needs GEMINI_API_KEY)",
    )
    parser.add_argument(
        "--limit", "-l", type=int, default=None,
        help="Only create the first N videos",
    )

    args = parser.parse_args()

    if args.voice == "elevenlabs" and not os.environ.get("ELEVENLABS_API_KEY"):
        print("ERROR: ELEVENLABS_API_KEY not set. Use --voice edge for free voiceover.")
        sys.exit(1)

    run_video_creation(
        scripts_path=args.scripts,
        output_dir=args.output_dir,
        voice_engine=args.voice,
        use_ai_images=args.ai_images,
        limit=args.limit,
    )


if __name__ == "__main__":
    main()
